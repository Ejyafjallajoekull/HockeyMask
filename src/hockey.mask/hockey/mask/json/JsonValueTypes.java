package hockey.mask.json;

/**
 * The JsonValueTypes enum contains all types a JSON value can be.
 * 
 * @author Planters
 *
 */
public enum JsonValueTypes {

	/**
	 * A JSON boolean.
	 */
	BOOLEAN,
	/**
	 * A JSON number.
	 */
	NUMBER,
	/**
	 * A JSON string.
	 */
	STRING,
	/**
	 * A JSON object.
	 */
	OBJECT,
	/**
	 * A JSON array.
	 */
	ARRAY,
	/**
	 * A JSON null.
	 */
	NULL
	
}
